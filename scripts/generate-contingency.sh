#!/usr/bin/env bash
set -euo pipefail

# Generates a comprehensive contingency/disaster recovery document
# with live system state. Called by backup.sh before uploading to Google Drive.

NANOCLAW_DIR="${NANOCLAW_DIR:-/Users/amrut/nanoclaw}"
OUTPUT="${1:-${NANOCLAW_DIR}/data/contingency.md}"

DB="${NANOCLAW_DIR}/store/messages.db"
NOW=$(date '+%Y-%m-%d %H:%M %Z')

# Helper: get DB value safely
dbval() { sqlite3 "$DB" "$1" 2>/dev/null || echo "N/A"; }

# Pull live R2 credentials from rclone config so the contingency doc is self-contained.
# This file lives in private Google Drive — safe to inline secrets.
RCLONE_CONF="${HOME}/.config/rclone/rclone.conf"
get_rclone_val() {
    awk -v section="$1" -v key="$2" '
        /^\[/ { in_sec = ($0 == "[" section "]") }
        in_sec && $0 ~ "^" key " *=" { sub(/^[^=]*= */, ""); print; exit }
    ' "$RCLONE_CONF" 2>/dev/null
}
R2_ACCESS_KEY=$(get_rclone_val r2 access_key_id)
R2_SECRET_KEY=$(get_rclone_val r2 secret_access_key)
R2_ENDPOINT=$(get_rclone_val r2 endpoint)
[ -z "$R2_ACCESS_KEY" ] && R2_ACCESS_KEY="MISSING-from-rclone-conf"
[ -z "$R2_SECRET_KEY" ] && R2_SECRET_KEY="MISSING-from-rclone-conf"
[ -z "$R2_ENDPOINT" ] && R2_ENDPOINT="https://b6a5767fe078a25e1a0f15cdf6c91fd4.r2.cloudflarestorage.com"

# Gather live stats
MSG_COUNT=$(dbval "SELECT count(*) FROM messages;")
CHAT_COUNT=$(dbval "SELECT count(*) FROM chats;")
ACTIVE_TASKS=$(dbval "SELECT count(*) FROM scheduled_tasks WHERE status='active';")
TASK_LIST=$(dbval "SELECT id || ' (' || schedule_type || ': ' || schedule_value || ')' FROM scheduled_tasks WHERE status='active';" | sed 's/^/- /')
DB_SIZE=$(du -sh "$DB" 2>/dev/null | cut -f1 || echo "N/A")
STORE_SIZE=$(du -sh "${NANOCLAW_DIR}/store" 2>/dev/null | cut -f1 || echo "N/A")
GROUPS_SIZE=$(du -sh "${NANOCLAW_DIR}/groups" 2>/dev/null | cut -f1 || echo "N/A")
R2_SIZE=$(timeout 30 rclone size r2:nanoclaw-backup --json --max-depth 0 2>/dev/null | grep -o '"bytes":[0-9]*' | cut -d: -f2 || echo "0")
R2_MB=$(( ${R2_SIZE:-0} / 1048576 ))

NODE_VER=$(node --version 2>/dev/null || echo "not installed")
RCLONE_VER=$(rclone version --check 2>/dev/null | head -1 || rclone --version 2>/dev/null | head -1 || echo "not installed")

# Services status
SERVICES=""
for svc in com.nanoclaw com.nanoclaw.backup com.nanoclaw.cloudflared com.nanoclaw.mlx-server; do
    if launchctl list 2>/dev/null | grep -q "$svc"; then
        SERVICES="${SERVICES}\n| ${svc} | Running |"
    else
        SERVICES="${SERVICES}\n| ${svc} | Stopped |"
    fi
done

# Git branch info
NC_BRANCH=$(git -C "$NANOCLAW_DIR" rev-parse --abbrev-ref HEAD 2>/dev/null || echo "N/A")
NC_COMMIT=$(git -C "$NANOCLAW_DIR" log -1 --format='%h %s' 2>/dev/null || echo "N/A")
# .env key names (no values)
ENV_KEYS=$(grep -oE '^[A-Z_][A-Z_0-9]+=' "${NANOCLAW_DIR}/.env" 2>/dev/null | sed 's/=//' | sort | sed 's/^/- /' || echo "- (none found)")

# WhatsApp auth status
WA1_STATUS="Missing"
WA2_STATUS="Missing"
[ -d "${NANOCLAW_DIR}/store/auth" ] && [ "$(ls -A "${NANOCLAW_DIR}/store/auth" 2>/dev/null)" ] && WA1_STATUS="Present ($(du -sh "${NANOCLAW_DIR}/store/auth" | cut -f1))"
[ -d "${NANOCLAW_DIR}/store/auth2" ] && [ "$(ls -A "${NANOCLAW_DIR}/store/auth2" 2>/dev/null)" ] && WA2_STATUS="Present ($(du -sh "${NANOCLAW_DIR}/store/auth2" | cut -f1))"

mkdir -p "$(dirname "$OUTPUT")"

cat > "$OUTPUT" << DOCEOF
