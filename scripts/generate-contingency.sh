## Specific Recovery Scenarios

### Scenario 1: WhatsApp Disconnected

Symptoms: No messages flowing, "connection lost" in logs.

\`\`\`bash
# Check service
launchctl list | grep nanoclaw

# Check logs
tail -50 ~/nanoclaw/logs/nanoclaw.error.log | grep -i "connect\|auth\|close"

# Restart service
launchctl unload ~/Library/LaunchAgents/com.nanoclaw.plist
launchctl load ~/Library/LaunchAgents/com.nanoclaw.plist

# If auth expired: re-scan QR code
# Open http://localhost:8080 or the cloudflared URL
# Follow QR scanning instructions
\`\`\`

### Scenario 2: Database Corruption

\`\`\`bash
# Stop service first
launchctl unload ~/Library/LaunchAgents/com.nanoclaw.plist

# Restore from R2
rclone copy r2:nanoclaw-backup/nanoclaw/store/messages.db ~/nanoclaw/store/

# Restart
launchctl load ~/Library/LaunchAgents/com.nanoclaw.plist
\`\`\`

### Scenario 3: Container Build Broken

\`\`\`bash
# Full clean rebuild
docker builder prune -af
./container/build.sh

# If Docker itself is broken, rebuild from scratch
# Install Docker Desktop or switch to Apple Container
\`\`\`

### Scenario 4: Lost .env File

\`\`\`bash
# Restore from R2
rclone copy r2:nanoclaw-backup/nanoclaw/env/ /tmp/env-restore/
mv /tmp/env-restore/dot-env ~/nanoclaw/.env
\`\`\`

### Scenario 5: Full Mac Replacement

Follow the "Quick Recovery" section at the top of this document.
Total recovery time: ~15 minutes (mostly npm install + container build).

